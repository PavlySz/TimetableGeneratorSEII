    class PBar implements Runnable {

        String name;

        public PBar(String s) {
            name = s;
        }

        public void run() {
            for (int i = 0; i <= 100; i++) {
                jProgressBar1.setValue(i);
                jProgressBar1.repaint();
                try {
                    Thread.sleep(15);
                } catch (Exception e) {
                }
            }
            if (name.equals("admin")) {
                JOptionPane.showMessageDialog(null, "Welcome " + name.toUpperCase(), "Welcome", 1);
                ChooseType r = new ChooseType();
                r.setTitle("Choose Data Type");
                r.setLocationRelativeTo(null);
                r.setVisible(true);
                dispose();
            }
        }
    }

