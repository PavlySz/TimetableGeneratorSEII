    private void SubmitButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButActionPerformed
        String u = textUsername.getText();
        char[] p = textPass.getPassword();
        if (u.equals("admin") && Arrays.equals("admin".toCharArray(), p)) {
            new Thread(new PBar(u)).start();
        } else if (!u.equals("admin") && !Arrays.equals("admin".toCharArray(), p)) {
            JOptionPane.showMessageDialog(this, "Invalid Username and Password");
        } else if (!u.equals("admin")) {
            JOptionPane.showMessageDialog(this, "Invalid Username");
        } else if (!Arrays.equals("admin".toCharArray(), p)) {
            JOptionPane.showMessageDialog(this, "Invalid Password");
        }
    }//GEN-LAST:event_SubmitButActionPerformed

